#编写应用于R软件的GM(1,1)模型
gm11<-function(x0,t){ #x0為輸入列，t為預測個數
  x1<-cumsum(x0) #一次累加產生序列1-AG0序列
  b<-numeric(length(x0)-1)
  n<-length(x0)-1
  for(i in 1:n){ #產生x1的緊鄰均值生成序列
    b[i]<--(x1[i]+x1[i+1])/2 
    b} #得序列b，即為x1的緊鄰均值產生序列
  D<-numeric(length(x0)-1)
  D[]<-1
  B<-cbind(b,D)
  BT<-t(B)#矩陣轉至
  M<-solve(BT%*%B)
  YN<-numeric(length(x0)-1)
  YN<-x0[2:length(x0)]
  alpha<-M%*%BT%*%YN  #模型的最小二乘估计参数列满足alpha
  alpha2<-matrix(alpha,ncol=1)
  a<-alpha2[1]
  u<-alpha2[2]
  cat("GM(1,1)參數估計值：",'\n',"發展係數-a=",-a,"  ","灰色用量u=",u,'\n','\n') #利用最小二乘法求得參數估計a,u
  y<-numeric(length(c(1:t)))
  y[1]<-x1[1]
  for(w in 1:(t-1)){  #將a,u的估計值代入時間響應序列函数计算x1擬合序列y
    y[w+1]<-(x1[1]-u/a)*exp(-a*w)+u/a 
  }
  cat("x(1)的模擬值：",'\n',y,'\n')
  xy<-numeric(length(y))
  xy[1]<-y[1]
  for(o in 2:t){ #運用後減運算還原得模型输入序列x0預測序列
    xy[o]<-y[o]-y[o-1] 
  } 
  cat("x(0)的模擬值：",'\n',xy,'\n','\n')                       
  #計算殘差e
  e<-numeric(length(x0))
  for(l in 1:length(x0)){
    e[l]<-x0[l]-xy[l] #得殘差
  }
  cat("殘差：",'\n',e,'\n')
  #計算相對殘差
  e2<-numeric(length(x0))
  for(s in 1:length(x0)){
    e2[s]<-(abs(e[s])/x0[s]) #得相對殘差
  }
  cat("相對殘差：",'\n',e2,'\n','\n')
  cat("殘差平方和=",sum(e^2),'\n')
  cat("平均相對误差=",sum(e2)/(length(e2)-1)*100,"%",'\n')
  cat("相對精準度=",(1-(sum(e2)/(length(e2)-1)))*100,"%",'\n','\n')
  #後驗差比值檢驗
  avge<-mean(abs(e));esum<-sum((abs(e)-avge)^2);evar=esum/(length(e)-1);se=sqrt(evar)  #计算殘差的變異數se
  avgx0<-mean(x0);x0sum<-sum((x0-avgx0)^2);x0var=x0sum/(length(x0));sx=sqrt(x0var)  #計算原序列x0的變異數sx
  cv<-se/sx  #得驗差比值
  cat("後驗差比值檢驗:",'\n',"C值=",cv,'\n')#對後驗差比值進行檢驗，與一般標準進行比較判断預測结果好壞。
  if(cv < 0.35){     
    cat("C值<0.35, GM(1,1)預測精準度等級為：好",'\n','\n')
  }else{
    if(cv<0.5){
      cat("C值屬於[0.35,0.5), GM(1,1)模型預測精準度等級為：合格",'\n','\n')
    }else{
      if(cv<0.65){
        cat("C值屬於[0.5,0.65), GM(1,1)模型預測精準度等級為：免強合格",'\n','\n')
      }else{
        cat("C值>=0.65, GM(1,1)模型預測精準度等級為：不合格",'\n','\n')
      }
    }
  }
  #畫出输入序列x0的預測序列圖
  plot(xy,col='blue',type='b',pch=16,xlab='時間序列',ylab='值')
  #points(x0,col='red',type='b',pch=4)
  legend('topleft',c('預測值'),pch=c(16,4),lty=l,col=c('blue'))
  #legend('topleft',c('预测价格','原始价格'),pch=c(16,4),lty=l,col=c('blue','red'))
}

a<-c(1.95,2.23,2.4,2.15,1.8,1.95) #輸入值

gm11(a,length(a)+6)


